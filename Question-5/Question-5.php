<?php

// For loop example
echo "For Loop Example:\n";
for ($i = 1; $i <= 5; $i++) {
    echo $i . "\n";
}

// While loop example
echo "\nWhile Loop Example:\n";
$j = 1;
while ($j <= 5) {
    echo $j . "\n";
    $j++;
}

// Do-While loop example
echo "\nDo-While Loop Example:\n";
$k = 1;
do {
    echo $k . "\n";
    $k++;
} while ($k <= 5);

?>
